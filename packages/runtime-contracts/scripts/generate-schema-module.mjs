import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const schemaRoot = resolve(packageRoot, "../../schemas/v0.3");
const outputPath = resolve(packageRoot, "src/generated-schemas.ts");
const schemaPaths = {
  config: "config.schema.json",
  conformanceClaim: "conformance-claim.schema.json",
  coreDiagnostic: "diagnostic.schema.json",
  operationResult: "operation-result.schema.json",
  queryResult: "query-result.schema.json",
  typeFile: "type-file.schema.json",
  provider: "runtime/provider.schema.json",
  action: "runtime/action.schema.json",
  event: "runtime/event.schema.json",
  capability: "runtime/capability.schema.json",
  workflow: "runtime/workflow.schema.json",
  runtimePolicy: "runtime/runtime-policy.schema.json",
  run: "runtime/run.schema.json",
  checkpoint: "runtime/checkpoint.schema.json",
  timer: "runtime/timer.schema.json",
  diagnostic: "runtime/diagnostic.schema.json",
  eventEnvelope: "runtime/event-envelope.schema.json",
};

const schemas = {};
for (const [name, relativePath] of Object.entries(schemaPaths)) {
  schemas[name] = JSON.parse(await readFile(resolve(schemaRoot, relativePath), "utf8"));
}

const source = `// Generated by scripts/generate-schema-module.mjs. Do not edit directly.\n\nexport const GENERATED_CANONICAL_SCHEMAS: Record<string, Record<string, unknown>> = ${JSON.stringify(schemas, null, 2)};\n`;
await writeFile(outputPath, source, "utf8");
